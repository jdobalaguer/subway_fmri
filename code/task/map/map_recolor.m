function map = map_recolor(map)
    
    subline_colors = {...
        [255.0000  198.4107  127.5000],...
        [136.9013  255.0000  127.5000],...
        [255.0000  127.5000  217.2329],...
        [127.5000  155.4953  255.0000] ...
    };
    subline_colors = subline_colors(randperm(length(subline_colors)));
    
    % stations
    for i_subline = 1:(.5*map.nb_sublines)
        map.sublines(2*i_subline-1).color = subline_colors{i_subline};
        map.sublines(2*i_subline  ).color = subline_colors{i_subline};
    end
